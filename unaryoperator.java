public class unaryoperator {
    public static void main(String [] args){
        int a=10;
        int b=15;
        System.out.println(++a);
        System.out.println(b++);
        System.out.println(b);
        int c=10;
        int d=-12;
        System.out.println(~c);
        System.out.println(~d);
        boolean e=true;
        boolean f=false;
        System.out.println(!e);
        System.out.println(!f);

    }
    
}
