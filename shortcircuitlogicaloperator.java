import java.util.*;

public class shortcircuitlogicaloperator {
    public static void main(String[] args) {
  
      /*
      
      &&
      ||

      */
      boolean a=true;
      boolean b=false;
      int c=12;
      
      System.out.println(a && b);
      System.out.println(a || b);
      System.out.println(c>20 && c<30);
      System.out.println(c>20 || c<30);
      
      
  }
}